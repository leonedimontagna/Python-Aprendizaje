import math


print(round(1.3)) # La función 'round' nos dará el número al que el número esté más cercano. 
print(round(1.7))
print(round(1.5))
print(abs(-77)) # La función 'abs' nos entregará el valor absoluto del número que nosotros le pasemos. Saca el signo del número que nosotros le estemos proporcionando. Positivo o Negativo. 
print(abs(55))

print(math.ceil(1.7)) # La función 'math.ceil' lo redondeará hacia el número más cercano, dependiendo si es 1.1 hacia 1 y si es 1.7 hacia 2. 
print(math.floor(1.99999)) # La función 'math.floor' lo redondeará hacia el número mas cercano hacia abajo. 
print(math.isnan(23)) # La función 'math.isnan' arrojará False si es un número, si le ponemos un string nos dará error. 
print(math.pow(10, 3)) # La función 'math.pow' es para elevar un número, en este caso 10 elevado a 3 que es 1000. 
print(math.sqrt(9)) # La función 'math.sqrt' nos sacará la raíz cuadrada del númeroque le indiquemos. 




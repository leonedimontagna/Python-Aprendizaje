# Operaciones Matemáticas en Python

print(1 + 3)
print(1 - 3)
print(1 * 3)
print(1 / 3)
# Este operador '//' no nos va a sacar los decimales, solo el número entero.
print(1 // 3)

# Este operador '%' nos saca el resto, lo que sobra de la división.
print(1 % 3)

# Este operador '**' hace que el número tenga exponente, en este sería 2 elevado a 3 que sería 8.
print(2 ** 3)


nombre = "Aitor"
apellido = "Ruiz"
nombre_completo = nombre + " " + apellido
print(nombre_completo)

nombre = "Aitor"
apellido = "Ruiz"
nombre_completo = f"{nombre} {apellido}"
print(nombre_completo)

nombre = "Aitor"
apellido = "Ruiz"
nombre_completo = f"{nombre[0]} {2 + 5}"
print(nombre_completo)

